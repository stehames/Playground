//adição
console.log(2+2);

//subtração
console.log(10-5);

//multiplicação
console.log(10*5);

//divisão
console.log(10/2);

//incremento
let num = 10;
console.log(++num);

//decremento
console.log(--num);

//comparação maior
console.log(10>5);

//comparação menor
console.log(10<5);

//comparação de menor ou igual
console.log(3<=3);

//comparação de maior ou igual
console.log(20>=15);

//comparação de igualdade
console.log(10==10);

//comparação igualdade rígido
console.log("5" === 5);

//comparação de diferente
console.log(10 != 10);

//comparação de diferente rígido
console.log(10 !== "10");

//comparador lógico &&
console.log(10>5 && 8 > 10);

//comparador lógico OR ||
console.log(10>5 || 8 > 10);

//comparacao de negação
console.log(!true);

